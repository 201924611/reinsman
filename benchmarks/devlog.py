"""Devlog generator — the harness writes its own weekly journal, from records only.

Appends one entry to DEVLOG.md derived mechanically from benchmarks/ledger.json
(latest run vs the previous one), starfuel/state.json, and the git log since the
previous benchmark run. No free-form claims: every line is computed from a record
a reader can open. Intended to run right after run_suite.py in the weekly routine.

Usage: python benchmarks/devlog.py
"""
from __future__ import annotations

import json
import subprocess
from pathlib import Path

BENCH_DIR = Path(__file__).resolve().parent
ROOT = BENCH_DIR.parent
DEVLOG = ROOT / "DEVLOG.md"
HEADER = (
    "# Devlog — written by the harness, from its own records\n\n"
    "Each entry is generated by [`benchmarks/devlog.py`](benchmarks/devlog.py) from the\n"
    "benchmark ledger, the star-fuel state, and the git log. Nothing here is written by hand.\n"
)


def git(*args: str) -> str:
    return subprocess.run(["git", *args], cwd=ROOT, capture_output=True,
                          text=True, encoding="utf-8").stdout.strip()


def main() -> int:
    runs = json.loads((BENCH_DIR / "ledger.json").read_text(encoding="utf-8"))
    if not runs:
        print("[devlog] no benchmark runs yet — nothing to write.")
        return 0
    cur, prev = runs[-1], (runs[-2] if len(runs) > 1 else None)

    lines = [f"\n## {cur['run_at'][:10]} — suite {cur['suite_overall']:.3f}"
             + (f" ({cur['suite_overall'] - prev['suite_overall']:+.3f} vs previous run)" if prev else " (baseline)")]

    for r in cur["results"]:
        delta = ""
        if prev:
            p = next((x for x in prev["results"] if x["id"] == r["id"]), None)
            if p is not None:
                delta = f" ({r['overall'] - p['overall']:+.2f})"
        lines.append(f"- `{r['id']}`: {r['overall']:.2f}{delta} — trace `{r['task_id']}`")

    if "fixtures_intact" in cur:
        lines.append("- fixtures integrity: " + ("intact ✔" if cur["fixtures_intact"] else "**TAMPERED — see run notes**"))

    since = prev["run_at"] if prev else cur["run_at"]
    commits = git("log", f"--since={since}", "--oneline", "--no-merges")
    n = len(commits.splitlines()) if commits else 0
    lines.append(f"- commits since previous run: {n}")

    sf = ROOT / "starfuel" / "state.json"
    if sf.exists():
        state = json.loads(sf.read_text(encoding="utf-8"))
        lines.append(f"- star-fuel unlocks so far: {len(state.get('unlocks', []))}")

    if not DEVLOG.exists():
        DEVLOG.write_text(HEADER, encoding="utf-8")
    with DEVLOG.open("a", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
    print(f"[devlog] entry appended for run {cur['run_at']}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
